const CUSTOM_DECKS_KEY = 'vocabGame_customDecks';

function getCustomDecks() {
  try {
    const data = localStorage.getItem(CUSTOM_DECKS_KEY);
    return data ? JSON.parse(data) : [];
  } catch (e) {
    console.error('Failed to load custom decks:', e);
    return [];
  }
}

function saveCustomDeck(deck) {
  const decks = getCustomDecks();
  decks.push(deck);
  localStorage.setItem(CUSTOM_DECKS_KEY, JSON.stringify(decks));
}

function deleteCustomDeck(deckId) {
  const decks = getCustomDecks().filter(function(d) { return d.id !== deckId; });
  localStorage.setItem(CUSTOM_DECKS_KEY, JSON.stringify(decks));
}

function getAllDecks() {
  return BUILTIN_DECKS.concat(getCustomDecks());
}

function parseCSV(csvText) {
  var lines = csvText.split('\n');
  var words = [];
  var id = 1;
  for (var i = 0; i < lines.length; i++) {
    var line = lines[i].trim();
    if (!line) continue;
    // Skip header row
    if (i === 0) {
      var lower = line.toLowerCase();
      if (lower.indexOf('en') !== -1 || lower.indexOf('english') !== -1) {
        continue;
      }
    }
    var parts = line.split(',');
    if (parts.length >= 2) {
      var en = parts[0].trim();
      var zh = parts[1].trim();
      if (en && zh) {
        words.push({ id: id, en: en, zh: zh });
        id++;
      }
    }
  }
  return words;
}
